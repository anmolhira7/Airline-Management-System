    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed

        String FirstName = firstName.getText();
        String LastName = lastName.getText();
        String Passport = passId.getText();
        String NIC = nicId.getText();
        Date d = (Date) date.getDate();
        String ADDRESS = address.getText();
        String Gender = (male.isSelected()) ? ("Male") : ("Female");
        String Contact = contact.getText();
        String CustomerId = cust_id.getText();

        DateFormat dt = new SimpleDateFormat("yyyy--MM--dd");
        String date = dt.format(d);

        //conection with db
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, e);
        }
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/airlinedatabase", "root", "1234");
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            //we have used ? as we don't know the values and we need to set them
            pre = con.prepareStatement("insert into customers(Customerid,FirstName,LastName,Passport,NIC_ID,Address,DOB,Gender,Contact) values(?,?,?,?,?,?,?,?,?)");
            pre.setString(1, CustomerId);
            pre.setString(2, FirstName);
            pre.setString(3, LastName);
            pre.setString(4, Passport);
            pre.setString(5, NIC);
            pre.setString(6, ADDRESS);
            pre.setString(7, date);
            pre.setString(8, Gender);
            pre.setString(9, Contact);
            //exexute INSERT query
            pre.executeUpdate();
            //if update query is successfull executed
            JOptionPane.showMessageDialog(null, "Customer Added");
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addButtonActionPerformed

