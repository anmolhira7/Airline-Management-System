    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String First_Name = first_name.getText();
        String User_Id = user_id.getText();
        String Last_Name = last_name.getText();
        String UserName = user_name.getText();
        String Password = password.getText();

        //conection with db
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
        } catch (ClassNotFoundException e) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, e);
        }
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost/airlinedatabase", "root", "1234");
        } catch (SQLException ex) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            //we have used ? as we don't know the values and we need to set them
            pre = con.prepareStatement("insert into users(UserId,FirstName,LastName,UserName,Password) values(?,?,?,?,?)");
            pre.setString(1, User_Id);
            pre.setString(2, First_Name);
            pre.setString(3, Last_Name);
            pre.setString(4, UserName);
            pre.setString(5, Password);

            //exexute INSERT query
            pre.executeUpdate();
            //if update query is successfull executed
            JOptionPane.showMessageDialog(null, "Admin User is Added");
        } catch (SQLException ex) {
            Logger.getLogger(AdminWindow.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addButtonActionPerformed

