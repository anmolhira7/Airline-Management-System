    public void autoId() {
        try {
            //legacy class com.mysql.jdbc.Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airlinedatabase", "root", "1234");
            //allow us to create statement with the help of this we can execute query
            Statement s = (Statement) con.createStatement();
            //store res of query here query is executed with help of jdbc driver
            ResultSet rs = s.executeQuery("Select MAX(UserId) from users");
            rs.next();
            //INITAL CUSOMER ID WHEN THERE IS NO users ID IN DB or when db is empty
            if (rs.getString("MAX(UserId)") == null) {
                //text var user id
                user_id.setText("FL001");
            } else {
                //get last id or max id from db add+1 to it
                long id = Long.parseLong(rs.getString("MAX(UserId)").substring(2, rs.getString("MAX(UserId)").length()));
                id++;
                user_id.setText("FL" + String.format("%03d", id));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

